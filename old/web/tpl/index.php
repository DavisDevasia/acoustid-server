<p>
 	<strong>Acoustid</strong> is an open source project that aims to create a
	free database of audio fingerprints with mapping to the <a href="http://musicbrainz.org">MusicBrainz</a>
	metadata database and provide a <a href="http://wiki.acoustid.org/wiki/Web_Service">web service</a> for audio file identification
	using this database.
</p>
<p>
	The content of the database is all submitted by users. If you have a MusicBraiz-tagged
	music collection, you can help by downloading our
	<a href="http://wiki.acoustid.org/wiki/Fingerprinter">submission tool</a> and letting it
	to analyze your audio files.
</p>
<p>
	All software components are open source, so if you are a developer interested in the project,
	you can download the source code for the <a href="http://wiki.acoustid.org/wiki/Chromaprint">client library</a>,
	the <a href="http://wiki.acoustid.org/wiki/Server">server application</a> and also the
	<a href="http://wiki.acoustid.org/wiki/Database">database</a> itself.
</p>
